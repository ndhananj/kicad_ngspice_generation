from __future__ import annotations

from datetime import date

from mixedsig2cad.compiled import compile_schematic
from mixedsig2cad.intent import build_schematic_intent
from mixedsig2cad.kicad_symbols import extract_project_symbol_block
from mixedsig2cad.projections.kicad import KiCadProjection, deterministic_uuid, project_geometry_to_kicad
from mixedsig2cad.spec import CircuitSpec


def _symbol_property(name: str, value: str, x: float, y: float, *, hidden: bool = False) -> list[str]:
    effects = "(effects (font (size 1.27 1.27)) hide)" if hidden else "(effects (font (size 1.27 1.27)))"
    return [
        f'    (property "{name}" "{value}" (at {x} {y} 0)',
        f"      {effects}",
        "    )",
    ]


def _wire(x1: float, y1: float, x2: float, y2: float, seed: str) -> list[str]:
    return [
        f"  (wire (pts (xy {x1:.2f} {y1:.2f}) (xy {x2:.2f} {y2:.2f}))",
        "    (stroke (width 0) (type solid) (color 0 0 0 0))",
        f"    (uuid {deterministic_uuid(seed)})",
        "  )",
    ]


def _text(text: str, x: float, y: float, seed: str) -> list[str]:
    return [
        f'  (text "{text}" (at {x:.2f} {y:.2f} 0)',
        "    (effects (font (size 1.27 1.27)))",
        f"    (uuid {deterministic_uuid(seed)})",
        "  )",
    ]


def _label(text: str, x: float, y: float, seed: str) -> list[str]:
    return [
        f'  (label "{text}" (at {x:.2f} {y:.2f} 0)',
        "    (effects (font (size 1.27 1.27)))",
        f"    (uuid {deterministic_uuid(seed)})",
        "  )",
    ]


def _junction(x: float, y: float) -> str:
    return f"  (junction (at {x:.2f} {y:.2f}) (diameter 1.016) (color 0 0 0 0))"


def _extract_symbol_block(symbol_name: str) -> str:
    return extract_project_symbol_block(symbol_name)


def _lib_symbols_block(projection: KiCadProjection) -> list[str]:
    lines = ["  (lib_symbols"]
    symbol_names = sorted({symbol.lib_id for symbol in projection.symbols})
    for symbol_name in symbol_names:
        block = _extract_symbol_block(symbol_name)
        for row in block.splitlines():
            lines.append(f"    {row}")
    lines.append("  )")
    return lines


def export_kicad_schematic(spec: CircuitSpec) -> str:
    intent = build_schematic_intent(spec)
    geometry = compile_schematic(intent)
    projection = project_geometry_to_kicad(geometry)
    return render_kicad_schematic(projection)


def render_kicad_schematic(projection: KiCadProjection) -> str:
    schematic_uuid = deterministic_uuid(f"sch:{projection.name}")
    lines: list[str] = [
        "(kicad_sch",
        "  (version 20231120)",
        '  (generator "mixedsig2cad")',
        f"  (uuid {schematic_uuid})",
        '  (paper "A4")',
        "  (title_block",
        f'    (title "{projection.name}")',
        f'    (date "{date.today().isoformat()}")',
        '    (comment 1 "Generated by mixedsig2cad")',
        "  )",
    ]
    lines.extend(_lib_symbols_block(projection))
    property_positions = {(text.owner_ref, text.role): text for text in projection.texts}

    for symbol in projection.symbols:
        placement = symbol.placement
        ref_text = property_positions.get((symbol.ref, "reference"))
        value_text = property_positions.get((symbol.ref, "value"))
        ref_x = ref_text.x if ref_text else placement.x
        ref_y = ref_text.y if ref_text else placement.y - 3.81
        value_x = value_text.x if value_text else placement.x
        value_y = value_text.y if value_text else placement.y + 3.81
        lines.extend(
            [
                f'  (symbol (lib_id "{symbol.lib_id}") (at {placement.x:.2f} {placement.y:.2f} {placement.angle}) (unit 1)',
                "    (in_bom yes) (on_board yes)",
                f"    (uuid {symbol.uuid})",
                *_symbol_property("Reference", symbol.ref, ref_x, ref_y, hidden=symbol.hidden_reference),
                *_symbol_property("Value", symbol.value, value_x, value_y),
                *_symbol_property("Footprint", "", 0, 0, hidden=True),
                *_symbol_property("Datasheet", "", 0, 0, hidden=True),
                "  )",
            ]
        )

    for text in projection.texts:
        if text.role in {"reference", "value"}:
            continue
        if text.role == "net_label":
            lines.extend(_label(text.text, text.x, text.y, text.uuid_seed))
        else:
            lines.extend(_text(text.text, text.x, text.y, text.uuid_seed))

    for wire in projection.wires:
        lines.extend(_wire(wire.x1, wire.y1, wire.x2, wire.y2, wire.uuid_seed))

    for junction in projection.junctions:
        lines.append(_junction(junction.x, junction.y))

    lines.extend(
        [
            "  (sheet_instances",
            '    (path "/" (page "1"))',
            "  )",
            "  (symbol_instances",
        ]
    )

    for symbol in projection.symbols:
        lines.append(
            f'    (path "/{symbol.uuid}" (reference "{symbol.ref}") (unit 1) (value "{symbol.value}") (footprint ""))'
        )

    lines.extend(["  )", ")"])
    return "\n".join(lines) + "\n"
